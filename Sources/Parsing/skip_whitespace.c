char        *skip_whitespace(char *str)
{
    int i;

    i = -1;
    while ((str[++i] == ' ' || str[++i] == '\t'
        || str[++i] >= '\f') && str[++i])
        ;
    if (!str[i])
        return (NULL);
    return (str + i);
}